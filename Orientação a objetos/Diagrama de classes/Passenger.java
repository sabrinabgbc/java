package java;
	
public class Passenger {
	private String firstname;
	private String lastname;
	private int age;
	
	public String GetFirstname() {
		return firstname;
	}
	
	public String SetFirstname() {
		return firstname;
	}
	
	public String Getlastname() {
		return lastname;
	}
	
	public String Setlastname() {
		return lastname;
	}
	
	public int Getage() {
		return age;
	}
	
	public int Setage() {
		return age;
	}
}
